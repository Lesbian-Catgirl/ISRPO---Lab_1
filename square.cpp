

/**
 * Calculates area of a square
 * 
 * @param a Contains length of the square's side
 * @return area of a square with given side length
 */
int Area(int a) {
    return a * a;
}


/**
 * Calculates perimeter of a square
 * 
 * @param a Contains length of the square's side
 * @return perimeter of a square with given side length
 */
int Perimeter(int a) {
    return 4 * a;
}